public class Student {
    private int sId;
    private String name;

    public int getsId() {
        return sId;
    }

    public void setsId(int sId) {
        this.sId = sId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Student(int sId, String name) {
        this.sId = sId;
        this.name = name;
    }
    

}
